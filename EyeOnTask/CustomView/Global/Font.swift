import UIKit

class Font {
    
   class func ColabRegular(fontSize : CGFloat) -> UIFont? {
        return UIFont(name: "Colaborate-Regular", size: fontSize)!
    }
    
   class func ColabBold(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "Colaborate-Bold", size: fontSize)!
    }
    
    class func ColabLight(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "ColaborateLight", size: fontSize)!
    }
    
   class func ColabMedium(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "Colaborate-Medium", size: fontSize)!
    }
    
   class func ArimoBoldItalic(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "Arimo-BoldItalic", size: fontSize)!
    }
    
   class func ArimoItalic(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "Arimo-Italic", size: fontSize)!
    }
    
   class func ArimoRegular(fontSize : CGFloat) -> UIFont {
    return UIFont(name: "Arimo-Regular.ttf", size: fontSize)!
    }
    
   class func ArimoBold(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "Arimo-Bold", size: fontSize)!
    }
}
